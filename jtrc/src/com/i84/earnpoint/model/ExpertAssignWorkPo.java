package com.i84.earnpoint.model;

import java.io.Serializable;

import com.i84.earnpoint.common.excel.ExcelAnnotation;

public class ExpertAssignWorkPo implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/*
	 * 专家编号 专家姓名 身份证号 登录密码 联系电话 单位名称 分配论文数
	 */
	@ExcelAnnotation(exportName = "专家编号")
	private String zpno;
	@ExcelAnnotation(exportName = "专家姓名")
	private String name;
	@ExcelAnnotation(exportName = "身份证号")
	private String pnum;
	@ExcelAnnotation(exportName = "登录密码")
	private String pwd;
	@ExcelAnnotation(exportName = "联系电话")
	private String tel;
	@ExcelAnnotation(exportName = "单位名称")
	private String company;
	@ExcelAnnotation(exportName = "分配论文数")
	private Integer lwnum;

	public String getZpno() {
		return zpno;
	}

	public void setZpno(String zpno) {
		this.zpno = zpno;
	}

	public String getPnum() {
		return pnum;
	}

	public void setPnum(String pnum) {
		this.pnum = pnum;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public Integer getLwnum() {
		return lwnum;
	}

	public void setLwnum(Integer lwnum) {
		this.lwnum = lwnum;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
